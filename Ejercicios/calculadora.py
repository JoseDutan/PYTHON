alto = float(input("Ingrese el Alto del rectángulo: "))
ancho = float(input("Ingrese el Ancho del rectángulo: "))

area = alto * ancho
perimetro = 2 * (alto + ancho)
superficie = area 

print(f"Área del rectángulo: {area}")
print(f"Perímetro del rectángulo: {perimetro}")
print(f"Superficie del rectángulo: {superficie}")